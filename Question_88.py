data1 = (1, 2)
data2 = (3, 4)

[print(sum(x)) for x in [data1 + data2]] # faz uma list comprehension, uma maneira concisa de criar listas.
                                         # para cada elemento presente nas tuplas concatenadas, realizada a soma e printa o resultado
# data1 + data2 = ((1, 2),(3, 4))
# sum(x) = 1 + 2 + 3 + 4 = 10 

